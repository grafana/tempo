package main

import (
	"fmt"
	"log"
	"os"
	"os/exec"

	"github.com/grafana/tempo/cmd/tempo/app"
	"go.yaml.in/yaml/v3"
)

const ManifestPath = "docs/sources/tempo/configuration/manifest.md"

// MCPConfigReferencePath is the embedded copy of the configuration reference served by
// the query-frontend MCP server. It is generated from the same default configuration as
// the website manifest so the two can never drift.
const MCPConfigReferencePath = "modules/frontend/docs/config-reference.md"

const Cmd = "go run pkg/docsgen/generate_manifest.go"

var Manifest = fmt.Sprintf(`---
title: Manifest
description: This manifest lists of all Tempo options and their defaults.
weight: 110
---
[//]: # 'THIS FILE IS GENERATED AUTOMATICALLY BY %s'
[//]: # 'DO NOT EDIT THIS FILE DIRECTLY'

# Manifest

This document is a reference for all Tempo options and their defaults. If you are just getting
started with Tempo, refer to [Tempo examples](https://github.com/grafana/tempo/tree/main/example/docker-compose)
and other [configuration documentation](../). Most installations will require only setting 10 to 20 of these options.

## Complete configuration

`, Cmd)

// MCPConfigReference is the header for the MCP-served configuration reference. It is plain
// markdown (no Hugo front matter) because it is embedded into the binary and returned
// verbatim to LLM clients.
var MCPConfigReference = fmt.Sprintf(`<!-- THIS FILE IS GENERATED AUTOMATICALLY BY %s -->
<!-- DO NOT EDIT THIS FILE DIRECTLY -->

# Tempo Configuration Reference

This is the complete reference of every Tempo configuration option and its default value,
rendered from the default configuration. Most installations only set 10 to 20 of these
options. For a guided overview of which block does what, request the "config-overview" doc.
For exact key names, nesting, and defaults, use the reference below.

## Complete configuration

`, Cmd)

func main() {
	newConfig := app.NewDefaultConfig()
	// Override values that depend on the host specifics
	const hostname = "hostname"
	newConfig.Distributor.DistributorRing.InstanceID = hostname
	newConfig.Generator.Ring.InstanceID = hostname
	newConfig.BackendWorker.Ring.InstanceID = hostname
	newConfig.Generator.InstanceID = hostname
	newConfig.BlockBuilder.InstanceID = hostname
	newConfig.LiveStore.Ring.InstanceID = hostname
	newConfig.LiveStore.Ring.InstanceInterfaceNames = []string{"eth0"}

	newConfigBytes, err := yaml.Marshal(newConfig)
	if err != nil {
		panic(err)
	}

	yamlBlock := "```yaml\n" + string(newConfigBytes) + "```\n"

	writeGenerated(ManifestPath, Manifest+yamlBlock)
	writeGenerated(MCPConfigReferencePath, MCPConfigReference+yamlBlock)
}

// writeGenerated writes content to path and fails if the on-disk file changed, mirroring
// the freshness guarantee enforced in CI.
func writeGenerated(path, content string) {
	if err := os.WriteFile(path, []byte(content), 0o644); err != nil {
		panic(err)
	}

	cmd := exec.Command("git", "diff", "--exit-code", path)
	cmd.Stdout = os.Stdout
	cmd.Stderr = os.Stderr
	if err := cmd.Run(); err != nil {
		log.Fatalf("%s has changed. Please run '%s' and commit the changes.", path, Cmd)
	}
}
